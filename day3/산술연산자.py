'''
1. + : 더하기 연산자
2. - : 빼기 연산자
3. * : 곱하기 연산자
4. / : 나누기 연산자
5. // : 나누기 몫 연산자
6. % : 나머지 연산자
7. ** : 거듭 제곱 연산자
'''

num1 = 100
num2 = 3

print(num1 + num2) #103
print(num1 - num2) #97
print(num1 * num2) #300
print(num1 / num2) #33.333333
print(num1 // num2) #33(나눈 몫만 가져오기 때문에 소수부의 손실이 일어남)
print(num1 % num2) #1
print(num2 ** num2) #3의 3제곱